package com.security.securitywithjpa.controller;


import com.security.securitywithjpa.model.Student;
import jakarta.servlet.http.HttpServletRequest;
import org.springframework.security.web.csrf.CsrfToken;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RestController;

import java.util.ArrayList;
import java.util.List;

@RestController
public class StudentController {

    private List<Student> students = new ArrayList<>(List.of(
            new Student("John", "Doe", 20),
            new Student("Jane", "Smith", 22),
            new Student("Alice", "Johnson", 19)
    ));

    @GetMapping("/")
    public String home() {
        return "Hello";
    }

//     Will not work because it is reserved by Spring Boot
//    @GetMapping("/error")
//    public String handleError() {
//        return "Route not found";
//    }

    @GetMapping("/students")
    public List<Student> getStudents() {
        return students;
    }

    @PostMapping("/students")
    public Student addStudent(@RequestBody Student student) {
        students.add(student);
        return student;
    }

    @GetMapping("/csrf-token")
    public CsrfToken getCsrfToken(HttpServletRequest request){
        return (CsrfToken) request.getAttribute("_csrf");
    }
}
